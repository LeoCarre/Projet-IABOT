import requests
from bs4 import BeautifulSoup
import csv

def scrape_job_data(url):
    # Ajouter un en-tête User-Agent à la requête
    headers = {'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36'}

    # Récupérer le contenu HTML de l'URL avec l'en-tête User-Agent
    response = requests.get(url, headers=headers)
    
    if response.status_code == 200:
        html_content = response.text

        # Parsez le contenu HTML avec BeautifulSoup
        soup = BeautifulSoup(html_content, 'html.parser')

        # Extract job title
        job_title = soup.find('h1').text.strip()

        # Extract job description under 'Le métier'
        job_description_section = soup.find('h2', {'class': 'border-decoration-bottom border-color-brand'}, text='Le métier')
        if job_description_section:
            job_description = job_description_section.find_next('div', {'class': 'ezrichtext-field'}).text.strip()
        else:
            job_description = "La section 'Le métier' n'a pas été trouvée."

        # Extract other information (you can customize as needed)
        competences_requises = soup.find('div', {'id': 'competences-requises'})
        ou_exercer = soup.find('div', {'id': 'ou-exercer'})
        etudes = soup.find('div', {'id': 'formations-diplomes'})
        emploi_secteur = soup.find('div', {'id': 'emploi-secteur'})
        salaire_debutant = soup.find('div', {'id': 'salaire-débutant'})

        # Prepare data for CSV
        data = {
            'Job Title': job_title,
            'Job Description': job_description,
            'Compétences Requises': competences_requises.text.strip() if competences_requises else "",
            'Où l\'exercer': ou_exercer.text.strip() if ou_exercer else "",
            'Les Études': etudes.text.strip() if etudes else "",
            'Emploi et Secteur': emploi_secteur.text.strip() if emploi_secteur else "",
            'Salaire du Débutant': salaire_debutant.text.strip() if salaire_debutant else ""
        }

        return data

    else:
        print(f"Erreur lors de la requête. Code de statut: {response.status_code}")
        return None

def scrape_multiple_urls(urls):
    # Initialize an empty list to store all data
    all_data = []

    for url in urls:
        print(f"Extraction des données depuis {url}")
        data = scrape_job_data(url)

        if data:
            # Add the data to the list
            all_data.append(data)

    return all_data

def save_to_csv(data_list):
    # Write to CSV
    csv_filename = 'all_job_data.csv'
    with open(csv_filename, 'w', newline='', encoding='utf-8') as csvfile:
        fieldnames = ['Job Title', 'Job Description', 'Compétences Requises', 'Où l\'exercer', 'Les Études', 'Emploi et Secteur', 'Salaire du Débutant']
        writer = csv.DictWriter(csvfile, fieldnames=fieldnames)

        # Write header
        writer.writeheader()

        # Write data
        for data in data_list:
            writer.writerow(data)

    print(f"Données extraites et enregistrées dans {csv_filename}")

